/** Flag basename ("US", "GB_ENG") for a Chess.com country id, or null if we have no art. */
export function flagCodeForCountryId(id) {
  if (id == null) return null;
  return COUNTRY_ID_TO_FLAG[String(id)] || null;
}

/** Display name ("United States") for a Chess.com country id, or null if unknown. */
export function countryNameForId(id) {
  if (id == null) return null;
  return COUNTRY_ID_TO_NAME[String(id)] || null;
}
